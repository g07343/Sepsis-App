-(void)animateFrameByFrame
{   //get path to each image instead of loading it as a UIImage object as that will kill memory requirements
    NSString *fullpath = [[[NSBundle mainBundle] bundlePath] stringByAppendingString:[introImages objectAtIndex:animationCounter]];
    
    introHolder.image = nil;
    introHolder.image = [UIImage imageWithContentsOfFile:fullpath];
    animationCounter ++;
    //end our timer object once we've displayed all of our images
    if (animationCounter == 647)
    {
        [animationTimer invalidate];
        animationTimer = nil;
        dismissTimer = [NSTimer scheduledTimerWithTimeInterval:4.0f target:self selector:@selector(dismissIntro) userInfo:nil repeats:NO];
    }
}


